"""Formatting helpers and Chinese labels."""


def fmt_billion(val):
    """Format money values consistently in 億台幣. Never use 'B'."""
    if abs(val) >= 1e8:
        return f"{val / 1e8:.0f} 億"
    if abs(val) >= 1e4:
        return f"{val / 1e4:.0f} 萬"
    return f"{val:,.0f}"


def fmt_billion_short(val):
    """Short format for chart annotations — always 億."""
    if abs(val) >= 1e8:
        return f"{val / 1e8:.0f}億"
    if abs(val) >= 1e4:
        return f"{val / 1e4:.0f}萬"
    return f"{val:,.0f}"


def fmt_pct(val):
    return f"{val:.1%}"


def fmt_years(quarters):
    if quarters < 0:
        return "未回本"
    return f"{quarters / 4:.1f}年"


def quarter_to_year_label(q):
    """Convert quarter index to year label."""
    return f"第{q // 4 + 1}年Q{q % 4 + 1}"


